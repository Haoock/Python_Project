#  回顾各个数据类型的基本定义

# 数字型
a = 3
b = 3.14
c = 3 + 4j
d = True
f = False
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(a, b, c, d)

# 字符串类型
s1 = "Hello World!"
s2 = 'Hello World!'
print(type(s1))
print(type(s2))
print(s1)
s = """Hello
World"""

#   列表类型
l1 = ['H', 'e', 'l', 'l', 'o']
print(type(l1))

# 元组类型
t1 = ('H', 2, 1, 1, '0')
print(type(t1))

# 字典类型
dic = {1: 'H', 2: 'e', 3: 'l', 4: 'l', 5: 'o'}
print(type(dic))

set1 = {'1', 2, 3, 'e', 'l'}
print(type(set1))
